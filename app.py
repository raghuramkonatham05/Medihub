@app.route("/", methods=["GET", "POST"])
def index():
    if not login_required():
        return redirect(url_for("login"))

    if request.method == "GET":
        return render_template("index.html", upload_done=False)

    try:
        file = request.files.get("report")
        if not file or file.filename == "":
            return render_template(
                "index.html",
                error="No file uploaded",
                upload_done=False
            )

        filename = f"{uuid.uuid4().hex}_{file.filename}"
        file_path = os.path.join(UPLOAD_FOLDER, filename)
        file.save(file_path)

        # OCR
        try:
            raw_text = extract_text(file_path)
            if not raw_text.strip():
                raw_text = "OCR_PENDING"
        except Exception:
            raw_text = "OCR_PENDING"

        if is_medihub_generated(raw_text):
            return render_template(
                "index.html",
                error="This report was generated by MediHub.",
                upload_done=False
            )

        lab_values = sanitize_lab_values(extract_lab_values(raw_text))
        flags = flag_lab_values(lab_values)
        patient = extract_patient_info(raw_text)

        # ✅ CLEAN OCR NOISE FROM NAME (FIXED LOCATION)
        if patient.get("name"):
            patient["name"] = (
                patient["name"]
                .replace("Age", "")
                .replace("AGE", "")
                .strip()
            )

        report_doc = {
            "user_id": session["user_id"],
            "original_file": filename,
            "raw_text": raw_text,
            "patient": patient,
            "lab_values": lab_values,
            "flags": flags,
            "created_at": datetime.utcnow()
        }

        result = reports.insert_one(report_doc)
        session["report_id"] = str(result.inserted_id)

        return render_template("index.html", upload_done=True)

    except Exception:
        traceback.print_exc()
        return render_template(
            "index.html",
            error="Unexpected error occurred",
            upload_done=False
        )


