# RESET PASSWORD
# =================================================
@app.route("/reset-password", methods=["GET", "POST"])
def reset_password():
    email = session.get("reset_email")
    if not email:
        return redirect(url_for("login"))

    if request.method == "POST":
        otp = request.form.get("otp")
        password = request.form.get("password")

        user = users.find_one({"email": email})
        if not user or user.get("reset_otp") != otp:
            return render_template("reset_password.html", error="Invalid OTP")

        if datetime.utcnow() > user.get("otp_expiry"):
            return render_template("reset_password.html", error="OTP expired")

        if not is_strong_password(password):
            return render_template("reset_password.html", error="Weak password")

        users.update_one(
            {"email": email},
            {
                "$set": {
                    "password": generate_password_hash(password, method="pbkdf2:sha256")
                },
                "$unset": {
                    "reset_otp": "",
                    "otp_expiry": ""
                }
            }
        )

        session.pop("reset_email", None)
        return render_template("reset_success.html")

    return render_template("reset_password.html")


