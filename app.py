@app.route("/analyze")
def analyze():
    if not login_required():
        return redirect(url_for("login"))

    report = reports.find_one(
        {"user_id": session["user_id"]},
        sort=[("created_at", -1)]
    )
    if not report:
        return redirect(url_for("index"))

    patient = report.get("patient", {})
    lab_values = report.get("lab_values", {})
    flags = report.get("flags", {})

    explanations = {
        t: explain(t, d.get("status", "normal"))
        for t, d in flags.items()
    }

    try:
        age = int(patient.get("age"))
    except Exception:
        age = None

    avatar = select_avatar(age, patient.get("gender"))
    report_types = detect_report_type(lab_values)

    results = {}
    if "CBC" in report_types:
        results["CBC"] = predict_cbc(lab_values)
        results["Anemia"] = predict_anemia_ml(lab_values)

    if "Diabetes" in report_types:
        results["Diabetes"] = predict_diabetes_ml(lab_values)

    if "Kidney" in report_types:
        results["Kidney"] = predict_kidney_ml(lab_values)

    return render_template(
        "dashboard.html",
        patient=patient,
        avatar=avatar,
        lab_values=lab_values,
        flags=flags,
        explanations=explanations,
        report_types=report_types,
        results=results,
        recommendations=["Maintain a healthy lifestyle."]
    )


